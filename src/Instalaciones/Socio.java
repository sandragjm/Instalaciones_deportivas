package Instalaciones;

/**
 * La clase SOCIO define a los socios que pertenecen
 * un centro y los que realizan una reserva
 *
 * @author Sandra
 * @version 1.0
 * @since 2021
 */

public class Socio {

    /** El nombre del socio*/
    public String nombre;
    /** La direccion del socio*/
    public String direccion;
    /** La ciudad en la que vive el socio*/
    public String ciudad;
    /** La provincia en la que reside el socio */
    public String provincia;
    /** El telefono del socio*/
    public String telefono;
    /** La cuota del socio*/
    public Float cuota;

}
